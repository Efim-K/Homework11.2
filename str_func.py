def output_text_capital(text):
    """
    Преобразование строки к верхнему регистру
    :param text:
    :return:
    """

    return text.upper()


def output_text_title(text):
    """
    Первую букву каждого слова переводит в верхний регистр
    :param text:
    :return:
    """
    return text.title()
